// 3.3. Write a ts program to check whether an alphabet is vowel or consonant using switch case.
var y = prompt("Enter an Alphabet: ");
switch (y) {
    case "a":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "A":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "e":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "E":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "i":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "I":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "o":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "O":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "u":
        console.log("".concat(y, " is a Vowel."));
        break;
    case "U":
        console.log("".concat(y, " is a Vowel."));
        break;
    default:
        console.log("This is consonant.");
}
